/**
 * MAIN.JS — Точка входа Vue-приложения
 * 
 * Здесь мы:
 * 1. Создаём экземпляр Vue-приложения
 * 2. Подключаем плагины (роутер)
 * 3. Монтируем приложение в DOM
 */

// Импортируем функцию создания приложения
import { createApp } from 'vue';

// Импортируем глобальные стили
import './style.css';

// Импортируем корневой компонент
import App from './App.vue';

// Импортируем роутер
import router from './router.js';

// Создаём экземпляр приложения
const app = createApp(App);

// Подключаем роутер
app.use(router);

// Монтируем приложение в элемент с id="app"
// (этот элемент находится в index.html)
app.mount('#app');
